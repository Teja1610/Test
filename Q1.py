# Initialize a list of five different fruits
fruits = ["Apple", "Banana", "Orange", "Grapes", "Mango"]

# Print the first fruit
first_fruit = fruits[0]
print(f"First fruit: {first_fruit}")

# Print the last fruit
last_fruit = fruits[-1]
print(f"Last fruit: {last_fruit}")

# Print the middle three fruits
middle_fruits = fruits[1:4]  # Slicing from index 1 to 3 (4 is exclusive)
print(f"Middle fruits: {middle_fruits}")
